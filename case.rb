fav_color = 'purple'
case fav_color
when 'red'
  puts "Red like fire!"
when 'orange'
 puts "Orange like, well... an orange."
when 'yellow'
  puts "Yellow daffodils are so pretty in the spring!"
when 'green'
  puts "Have you been to the Emerald City in Oz?"
when 'blue'
  puts "Blue like the sky!"
when 'purple'
  puts "Purple plums are the tastiest."
else
  puts "Hmm, well I don't know what that color is!"
end

the_weather = 'cloudy'
case the_weather
when 'sunny'
  puts "Sun! Glorious Sun!"
when 'cloudy'
  puts "Doomy Gloomy"
when 'foggy'
  puts "Gee, I can't see a thing!"
when 'rainy'
  puts "Rain, rain, go away!"
end
