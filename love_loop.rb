all_love_notes = [
  "You are my sunshine.",
  "You're the coolest.",
  "Let's cuddle.",
  "It's date night.",
]

total_love_notes = all_love_notes.size
notes_displayed = 0

while (notes_displayed <= total_love_notes)
  puts all_love_notes[notes_displayed]
  notes_displayed += 1
  puts notes_displayed
end
